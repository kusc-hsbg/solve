SELECT
  DATE_FORMAT(SALES_DATE, '%Y-%m-%d') AS SALES_DATE,
  PRODUCT_ID,
  USER_ID,
  SALES_AMOUNT
FROM
  (
    SELECT
      SALES_DATE,
      PRODUCT_ID,
      NULL AS USER_ID,
      SALES_AMOUNT
    FROM
      OFFLINE_SALE
    WHERE
      EXTRACT(YEAR FROM SALES_DATE) = 2022
      AND EXTRACT(MONTH FROM SALES_DATE) = 3
    UNION ALL
    SELECT
      SALES_DATE,
      PRODUCT_ID,
      USER_ID,
      SALES_AMOUNT
    FROM
      ONLINE_SALE
    WHERE
      EXTRACT(YEAR FROM SALES_DATE) = 2022
      AND EXTRACT(MONTH FROM SALES_DATE) = 3
  ) AS combined_sales
ORDER BY
  SALES_DATE ASC,
  PRODUCT_ID ASC,
  USER_ID ASC;